namespace local_kopere_dashboard\util;

/**
 * Class request
 */
class request {

    /**
     * Function user_agent
     *
     * @return string|null
     */
    public static function user_agent(): ?string {
        if (!empty($_SERVER["HTTP_USER_AGENT"])) {
            return $_SERVER["HTTP_USER_AGENT"];
        }
        return null;
    }
}
